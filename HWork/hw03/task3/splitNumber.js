// Отримання п'ятизначного числа від користувача
const number = prompt("Введіть п'ятизначне число:");

// Перевірка чи введено саме п'ятизначне число
if (number.length === 5 && !isNaN(number)) {
  // Розкладання числа за цифрами і виведення їх через пробіл
  console.log(number.split('').join(' '));
} else {
  console.log("Будь ласка, введіть правильне п'ятизначне число.");
}
